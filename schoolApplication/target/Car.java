package schoolApplication.model;

public class Car {
	
	private String name;
	private String speed;
	
	public void setName(String name){
		this.name = name;
	}
	
	public String getName(){
		return name;
	}
	
	public void setSpeed(String speed){
		this.speed = speed;
	}
	
	public String getSpeed(){
		return speed;
	}

}
